package javaapplication5;
public class Dikdortgen {
        
    private int en;
    private int boy;
    public Dikdortgen(int en, int boy) {
        this.en = en;
        this.boy = boy;
    }
    public void setEn(int en) {
        this.en = en;
    }
    public void setBoy(int boy) {
        this.boy = boy;
    }
    public int getEn() {
        return en;
    }
    public int getBoy() {
        return boy;
    }
    
}
